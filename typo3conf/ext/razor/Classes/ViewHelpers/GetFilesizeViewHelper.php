use TYPO3\CMS\Fluid\Core\ViewHelper\AbstractViewHelper;

/**
 * Class to get the file size of a file
 *
 * @author Raphael Zschorsch <rafu1987@gmail.com>
 */
class GetFilesizeViewHelper extends AbstractViewHelper
{

    /**
     * Get filesize
     *
     * @param string $file
     * @return string
     */
    public function render($file)
    {
        if (is_file($file)) {
            return $this->formatSizeUnits(filesize($file));
        }
    }

    public function formatSizeUnits($bytes)
    {
        if ($bytes >= 1073741824) {
            $bytes = number_format($bytes / 1073741824, 2) . ' GB';
        } elseif ($bytes >= 1048576) {
            $bytes = number_format($bytes / 1048576, 2) . ' MB';
        } elseif ($bytes >= 1024) {
            $bytes = number_format($bytes / 1024, 2) . ' KB';
        } elseif ($bytes > 1) {
            $bytes = $bytes . ' bytes';
        } elseif ($bytes == 1) {
            $bytes = $bytes . ' byte';
        } else {
            $bytes = '0 bytes';
        }

        return $bytes;
    }

}
